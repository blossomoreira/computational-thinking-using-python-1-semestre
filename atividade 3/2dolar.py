# •	Tendo como base o valor da cotação do dólar (em reais) do dia,
# faça a conversão de um valor em dólares para reais.

cotacao = float(input("Digte aqui a cotação do dolar"))
valor_dolar = float(input("Digite aqui o valor do dolar"))

real = cotacao * valor_dolar

print("Este é o valor em reais: ", real)